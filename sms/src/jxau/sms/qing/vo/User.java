package jxau.sms.qing.vo;

public class User {
	private String no; //学号或者教工编号
	private String password;
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
}
